package pt.ulisboa.tecnico.sdis.id.ws.cli;

public class SDIdClientException extends Exception {

	public SDIdClientException() {
	}

	public SDIdClientException(String message) {
		super(message);
	}

	public SDIdClientException(Throwable cause) {
		super(cause);
	}

	public SDIdClientException(String message, Throwable cause) {
		super(message, cause);
	}

}